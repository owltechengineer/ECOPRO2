// AGGREGATED GLOBAL KPIs
// ─────────────────────────────────────────────

export function getGlobalKPIs(): ActivityKPIs & { totalCapital: number } {
  const actives = MOCK_ACTIVITIES.filter((a) => a.isActive && a.kpis);
  const kpis = actives.map((a) => a.kpis!);

  const totalRevenue = kpis.reduce((s, k) => s + k.totalRevenue, 0);
  const totalCosts = kpis.reduce((s, k) => s + k.totalCosts, 0);
  const grossMargin = totalRevenue - totalCosts;
  const totalCapital = actives.reduce((s, a) => s + a.capitalInvested, 0);

  return {
    totalRevenue,
    totalCosts,
    grossMargin,
    grossMarginPct: totalRevenue > 0 ? (grossMargin / totalRevenue) * 100 : 0,
    netMargin: grossMargin * 0.85,
    netMarginPct: totalRevenue > 0 ? ((grossMargin * 0.85) / totalRevenue) * 100 : 0,
    ebitda: grossMargin * 0.88,
    ebitdaPct: totalRevenue > 0 ? ((grossMargin * 0.88) / totalRevenue) * 100 : 0,
    roi: calculateGlobalROI(kpis, totalCapital),
    paybackPeriodMonths: 0,
    cashFlow: kpis.reduce((s, k) => s + k.cashFlow, 0),
    burnRate: kpis.reduce((s, k) => s + k.burnRate, 0),
    runwayMonths: Math.min(...kpis.map((k) => k.runwayMonths)),
    revenueGrowthRate: avg(kpis.map((k) => k.revenueGrowthRate)),
    costVariance: avg(kpis.map((k) => k.costVariance)),
    revenueVariance: avg(kpis.map((k) => k.revenueVariance)),
    productivityIndex: avg(kpis.map((k) => k.productivityIndex)),
    healthScore: avg(kpis.map((k) => k.healthScore)),
    budgetUtilizationPct: avg(kpis.map((k) => k.budgetUtilizationPct)),
    period: "2025",
    totalCapital,
  };
}

function avg(arr: number[]): number {
  return arr.length === 0 ? 0 : arr.reduce((a, b) => a + b, 0) / arr.length;
}

function calculateGlobalROI(
  kpis: ActivityKPIs[],
  totalCapital: number
): number {
  const totalGain = kpis.reduce((s, k) => s + k.grossMargin, 0);
  if (totalCapital === 0) return 0;
  return ((totalGain - totalCapital) / totalCapital) * 100;
}
